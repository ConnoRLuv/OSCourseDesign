public class PageMappingTable {
    private int PageNum;
    private int PageFrameNum;
    private int InterruptBit;

    public int getPageNum() {
        return PageNum;
    }

    public void setPageNum(int pageNum) {
        PageNum = pageNum;
    }

    public int getPageFrameNum() {
        return PageFrameNum;
    }

    public void setPageFrameNum(int pageFrameNum) {
        PageFrameNum = pageFrameNum;
    }

    public int getInterruptBit() {
        return InterruptBit;
    }

    public void setInterruptBit(int interruptBit) {
        InterruptBit = interruptBit;
    }



}
